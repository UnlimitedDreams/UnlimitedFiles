package Modelo;

/**
 *
 * @author Britt
 */
public class ArchivoDesaprobado {
    private String correo;
    private String titulo;
    private String cedula;

    public ArchivoDesaprobado(String correo, String titulo, String cedula) {
        this.correo = correo;
        this.titulo = titulo;
        this.cedula = cedula;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }
    
    
}
